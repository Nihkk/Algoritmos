dia = int(input("Escolha um número entre 1 e 7: "))

if dia == 1:
    print(f"Você escolheu Domingo.")

elif dia == 2:
    print(f"Você escolheu Segunda.")

elif dia == 3:
    print(f"Você escolheu Terça.")

elif dia == 4:
    print(f"Você escolheu Quarta.")

elif dia == 5:
    print(f"Você escolheu Quinta.")

elif dia == 6:
    print(f"Você escolheu Sexta.")

elif dia == 7:
    print(f"Você escolheu Sábado.")

else: 
    print(f"!@!@!@ Valor inválido @!@!@!")


